    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (ValidarCampos()) {
            String rut = this.txt_rut.getText();
            String clave = this.txt_clave.getText();
            String login = t.Login(rut, clave);
            if (login.equals("admin")) {
                TraLogin = t.buscarTradbajador(rut);
                InicioAdmin v = new InicioAdmin();
                this.setVisible(false);
                v.setVisible(true);
                v.setResizable(false);
                v.setLocationRelativeTo(null);
            } else if (login.equals("trabajador")) {
                TraLogin = t.buscarTradbajador(rut);
                VentanaTrabajador v = new VentanaTrabajador();
                this.setVisible(false);
                v.setVisible(true);
                v.setResizable(false);
                v.setLocationRelativeTo(null);
            } else {
                JOptionPane.showMessageDialog(null, login);
                this.txt_clave.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Todos Los Campos Son Obligatorios");
            this.txt_clave.setText("");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

