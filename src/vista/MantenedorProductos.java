    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        if (camposNull()) {
            try {
                pp.setCodigo(Integer.parseInt(this.txt_Codigo.getText()));
                pp.setDetalle(this.txt_descripcion.getText());
                pp.setValor(Integer.parseInt(this.txt_Valor.getText()));
                if (this.checkActivo.isSelected()) {
                    pp.setEstado(new estado(1, "activo"));
                } else {
                    pp.setEstado(new estado(2, "Inactivo"));
                }
                int codcat = Integer.parseInt(this.ComboCategoria.getSelectedItem().toString().substring(0, 1));
                pp.setCategoria(new categoria(codcat, "Categoria"));
                JOptionPane.showMessageDialog(null, p.ActualizarProducto(pp));
                limpiar();
                limpiarTabla();
                apro.removeAll(apro);
                p.cargarProductos(apro);
                cargarTabla();
                this.panelCampos.setVisible(false);
                this.BotonIKngresar.setVisible(true);
                this.BotonEliminar.setVisible(true);
                this.BotonEditar.setVisible(true);
            } catch (java.lang.NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "El campo 'Valor' deve ser numerico ");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Todos Los Campos Son Obligatorios");
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

