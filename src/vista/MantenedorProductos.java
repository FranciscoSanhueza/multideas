    private void BotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEliminarActionPerformed
        int fila = this.tablaTranajador.getSelectedRow();
        if (fila >= 0) {
            int codigo = Integer.parseInt(this.tablaTranajador.getValueAt(fila, 0).toString());
            JOptionPane.showMessageDialog(null, p.CambiarEstado(codigo));
            limpiar();
            limpiarTabla();
            apro.removeAll(apro);
            p.cargarProductos(apro);
            cargarTabla();
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un producto para desactivar");
        }
    }//GEN-LAST:event_BotonEliminarActionPerformed

    private void BotonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarActionPerformed
        if(this.tablaTranajador.getSelectedRow() >= 0){
        try {
            this.jButton1.setVisible(false);
            this.botonGuardar.setVisible(true);
            this.panelCampos.setVisible(true);
            this.BotonIKngresar.setVisible(false);
            this.BotonEditar.setVisible(false);
            this.BotonEliminar.setVisible(false);
            this.checkActivo.setVisible(true);
            this.checkInactivoEstado.setVisible(true);
            this.labelEstado.setVisible(true);
            this.txt_Codigo.setEditable(false);
            int fila = this.tablaTranajador.getSelectedRow();
            int cod = Integer.parseInt(this.tablaTranajador.getValueAt(fila, 0).toString());
            for (producto a : apro) {
                if (a.getCodigo() == cod) {
                    this.txt_Codigo.setText(a.getCodigo() + "");
                    this.txt_descripcion.setText(a.getDetalle());
                    this.txt_Valor.setText(a.getValor() + "");
                    this.ComboCategoria.setSelectedIndex((a.getCategoria().getCodigo()) - 1);
                    if (a.getEstado().getCodigo() == 1) {
                        this.checkActivo.setSelected(true);
                    } else {
                        this.checkInactivoEstado.setSelected(true);
                    }
                    break;
                }
            }
        } catch (java.lang.NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "seleccione un producto antes de editar");
        }
        }else{
            JOptionPane.showMessageDialog(null, "seleccione un producto antes de editar");
        }
    }//GEN-LAST:event_BotonEditarActionPerformed

