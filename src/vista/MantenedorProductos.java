    public void ComboCategoria() {
        if (!cc.isEmpty()) {
            for (categoria cat : cc) {
                this.ComboCategoria.addItem(cat.getCodigo() + ") " + cat.getDetalle());
            }
        } else {
            String[] a = new String[1];
            a[0] = "no existen categorias Disponibles";
            modelo.addRow(a);
        }
    }

    public void cargarTabla() {
        if (!apro.isEmpty()) {
            if (!this.checkInactivosTabla.isSelected()) {
                for (producto tr : apro) {
                    String[] a = new String[5];
                    if (tr.getEstado().getCodigo() == 1) {
                        a[0] = tr.getCodigo() + "";
                        a[1] = tr.getDetalle();
                        a[2] = tr.getValor() + "";
                        a[3] = tr.getCategoria().getDetalle();
                        a[4] = tr.getEstado().getDetalle();
                        modelo.addRow(a);
                    }
                }
            } else {
                for (producto tr : apro) {
                    String[] a = new String[5];
                    a[0] = tr.getCodigo() + "";
                    a[1] = tr.getDetalle();
                    a[2] = tr.getValor() + "";
                    a[3] = tr.getCategoria().getDetalle();
                    a[4] = tr.getEstado().getDetalle();
                    modelo.addRow(a);
                }
            }
        } else {
            String[] a = new String[1];
            a[0] = "no existen Productos Para Mostrar";
            modelo.addRow(a);
        }
    }

    public void limpiar() {
        this.txt_Codigo.setText("");
        this.txt_descripcion.setText("");
        this.txt_Valor.setText("");
        this.checkActivo.setSelected(false);
        this.checkInactivoEstado.setSelected(false);
        this.ComboCategoria.setSelectedIndex(0);
    }

    public void limpiarTabla() {
        for (int i = 0; i < this.tablaTranajador.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }

    public boolean camposNull() {
        if (!(this.txt_Valor.getText().equals("") || this.txt_descripcion.getText().equals(""))) {
            if (!(this.txt_Valor.getText().substring(0, 1).equals(" ") || this.txt_descripcion.getText().substring(0, 1).equals(" "))) {
                return true;
            } else {
                return false;
            }
        } else {
            return false;
        }
    }

